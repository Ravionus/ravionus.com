// ── 7. Word Frequency ──────────────────────────────────────────────────────
test.describe('Text Statistics — word frequency', () => {
    test('repeated word appears in frequency chart', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FREQ_TEXT);
        await expect(page.locator('#freqChart')).toContainText('cat');
    });

    test('most frequent word listed first', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FREQ_TEXT); // cat×3, dog×2, bird×1
        const firstWord = await page.locator('#freqChart .freq-word').first().textContent();
        expect(firstWord).toBe('cat');
    });

    test('count shown next to word', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FREQ_TEXT);
        const firstCount = await page.locator('#freqChart .freq-count').first().textContent();
        expect(firstCount).toBe('3');
    });

    test('empty input shows placeholder message', async ({ page }) => {
        await page.goto(URL);
        await page.evaluate(() => localStorage.clear());
        await page.reload();
        await expect(page.locator('#freqChart')).toContainText('Type something');
    });

    test('stopword toggle checkbox is present', async ({ page }) => {
        await page.goto(URL);
        await expect(page.locator('#ignoreStopWords')).toBeVisible();
    });

    test('ignoring stopwords removes common words from chart', async ({ page }) => {
        await page.goto(URL);
        // Fill with content containing stopwords
        await page.locator('#textInput').fill('the cat the cat sat on the mat');
        // 'the' appears 3 times and is a stopword
        // Without ignore: 'the' should be top
        const topBefore = await page.locator('#freqChart .freq-word').first().textContent();
        expect(topBefore).toBe('the');

        // Enable stopword filtering
        await page.check('#ignoreStopWords');
        // 'the' should no longer be top
        const topAfter = await page.locator('#freqChart .freq-word').first().textContent();
        expect(topAfter).not.toBe('the');
    });
});

// ── 8. Toolbar ─────────────────────────────────────────────────────────────
test.describe('Text Statistics — toolbar', () => {
    test('Sample button fills the textarea', async ({ page }) => {
        await page.goto(URL);
        await page.evaluate(() => localStorage.clear());
        await page.reload();
        await page.click('#btnSample');
        const val = await page.inputValue('#textInput');
        expect(val.trim().length).toBeGreaterThan(20);
    });

    test('Sample button updates word count', async ({ page }) => {
        await page.goto(URL);
        await page.evaluate(() => localStorage.clear());
        await page.reload();
        await page.click('#btnSample');
        const words = await page.locator('#statWords').textContent();
        expect(parseInt(words, 10)).toBeGreaterThan(0);
    });

    test('Clear button empties the textarea', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FIVE_WORDS);
        await page.click('#btnClear');
        const val = await page.inputValue('#textInput');
        expect(val).toBe('');
    });

    test('Clear button resets word count to 0', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FIVE_WORDS);
        await expect(page.locator('#statWords')).toHaveText('5');
        await page.click('#btnClear');
        await expect(page.locator('#statWords')).toHaveText('0');
    });

    test('Clear button resets all stats to 0', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FIVE_WORDS);
        await page.click('#btnClear');
        await expect(page.locator('#statChars')).toHaveText('0');
        await expect(page.locator('#statLines')).toHaveText('0');
    });

    test('Copy Stats copies text containing "Words"', async ({ page }) => {
        await page.goto(URL);
        await page.context().grantPermissions(['clipboard-read', 'clipboard-write']);
        await page.locator('#textInput').fill(FIVE_WORDS);
        // Wait for debounce to fire before copying
        await expect(page.locator('#statWords')).toHaveText('5');
        await page.click('#btnCopyStats');
        const clip = await page.evaluate(() => navigator.clipboard.readText());
        expect(clip).toContain('Words');
        expect(clip).toContain('5');
    });

    test('Copy Stats copies reading time', async ({ page }) => {
        await page.goto(URL);
        await page.context().grantPermissions(['clipboard-read', 'clipboard-write']);
        await page.locator('#textInput').fill(FIVE_WORDS);
        // Wait for debounce to fire before copying
        await expect(page.locator('#statWords')).toHaveText('5');
        await page.click('#btnCopyStats');
        const clip = await page.evaluate(() => navigator.clipboard.readText());
        expect(clip).toContain('Reading time');
    });

    test('no dialog fired on any toolbar action', async ({ page }) => {
        await page.goto(URL);
        let dialogFired = false;
        page.on('dialog', () => { dialogFired = true; });
        await page.locator('#textInput').fill(FIVE_WORDS);
        await page.click('#btnClear');
        await page.click('#btnSample');
        await page.click('#btnCopyStats');
        expect(dialogFired).toBeFalsy();
    });
});

