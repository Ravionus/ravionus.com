// ── 11. localStorage ───────────────────────────────────────────────────────
test.describe('Text Statistics — localStorage', () => {
    test('typed text persists after reload', async ({ page }) => {
        await page.goto(URL);
        await page.evaluate(() => localStorage.clear());
        await page.reload();
        await page.locator('#textInput').fill(FIVE_WORDS);
        await page.waitForTimeout(400); // allow debounce
        await page.reload();
        const val = await page.inputValue('#textInput');
        expect(val).toBe(FIVE_WORDS);
    });

    test('stats auto-computed from stored text on reload', async ({ page }) => {
        await page.goto(URL);
        await page.evaluate((k) => localStorage.setItem(k, 'hello world'), 'ravionus_tool_text_stats');
        await page.reload();
        await expect(page.locator('#statWords')).toHaveText('2');
    });

    test('cleared text reflected in localStorage', async ({ page }) => {
        await page.goto(URL);
        await page.locator('#textInput').fill(FIVE_WORDS);
        await page.waitForTimeout(400);
        await page.click('#btnClear');
        await page.waitForTimeout(400);
        const saved = await page.evaluate(() => localStorage.getItem('ravionus_tool_text_stats'));
        expect(saved ?? '').toBe('');
    });
});

