const BASE = process.env.BASE_URL || 'http://localhost:3000';
const URL  = `${BASE}/tools/text-stats/`;

// ── Fixed test inputs with pre-verified expected values ──

// 'hello' → words:1, chars:5, charsNoSpace:5, lines:1, sentences:0, paragraphs:1
const ONE_WORD = 'hello';

// 'hello world' → words:2, chars:11, charsNoSpace:10, lines:1, sentences:0, paragraphs:1
const TWO_WORDS = 'hello world';

// 'one two three four five' → words:5
const FIVE_WORDS = 'one two three four five';

// 'Hi. Bye. Done.' → sentences:3
const THREE_SENTENCES = 'Hi. Bye. Done.';

// 'a\nb\nc' → lines:3
const THREE_LINES = 'a\nb\nc';

// 'block one text.\n\nblock two text.' → paragraphs:2
const TWO_PARAS = 'block one text.\n\nblock two text.';

// 'cat cat cat dog dog bird' → cat×3, dog×2, bird×1
const FREQ_TEXT = 'cat cat cat dog dog bird';

// ~200 words for reading time test (generated by repeat)
const READING_200 = Array(40).fill('hello world foo bar baz').join(' ');
// 40 groups × 5 words = 200 words → 60 sec → "1 min"

